#include <unistd.h>

int	main(int ac, char **av)
{
	char	*str;
	int	i;

	i = 1;
	while (i < ac)
	{
		str = *(av + i);
		while (*str)
		{
			write (1, str, 1);
			str++;
		}
		write (1, "\n", 1);
		i++;
	}
}
