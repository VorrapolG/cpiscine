#include <unistd.h>

int	main(int ac, char **av)
{
	char	*str;
	int		i;

	i = ac - 1;
	while (i != 0)
	{
		str = *(av + i);
		while (*str)
		{
			write(1, str, 1);
			str++;
		}
		write(1, "\n", 1);
		i--;
	}
}
